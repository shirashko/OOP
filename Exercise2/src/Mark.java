/**
 * Enumeration representing the possible marks on a Tic-Tac-Toe board.
 * Each cell of the Tic-Tac-Toe board can be marked with one of these values.
 *
 * @author Shir Rashkovits
 */
public enum Mark { // TODO: check if have to document X, O, BLANK because they are self-explanatory
    /**
     * Represents a blank state of a cell on the board.
     */
    BLANK,

    /**
     * Represents the 'X' mark on the board.
     */
    X,

    /**
     * Represents the 'O' mark on the board.
     */
    O;
}
